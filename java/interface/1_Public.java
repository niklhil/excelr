/* 
     An interface in the Java programming language is an abstract type that is used to declare behavior that classes must implement.
     Interface should be named in adjective or noun form.
     Interface can't be directly instantiated.
     Interface members are public by default. 
     Interface normally contains abstract methods and constants.
*/

interface Drinkable
{
     int CUP=1;
     void drink();
     void taste();
}










